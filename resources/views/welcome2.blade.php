                    compania = $('#compania').val()
                    comentarios = $('#comentarios').val()

                    if(nombre == '' || telefono == '' || compania == '' || comentarios == ''){
                        alert('Favor de llenar todos los campos')
                        return false
                    }
                    $.ajax({
                            url:'/send/contact',
                            type:'POST',
                            dataType: 'json',
                            headers: {
                                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                            },
                            data: { nombre: nombre, telefono:telefono, compania:compania, comentarios:comentarios },
                            success: function(response){
                                if(response.send == null){
                                    $('#nombre').val('')
                                    $('#telefono').val('')
                                    $('#compania').val('')
                                    $('#comentarios').val('')
                                    $('.gracias').show()
                                    //alert('Gracias por ponerte en contacto, en breve nos comunicaremos contigo')
                                }
                            },
                            error: function(data){
                                var errors = data.responseJSON;
                                console.log(errors);
                            }
                        })
                    return false;
                })
